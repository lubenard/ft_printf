#include "../ft_printf.h"

int		ft_strchr(char const *s, int c)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return (i);
		++i;
	}
	if (s[i] == '\0' && c == '\0')
		return (i);
	return (-1);
}
